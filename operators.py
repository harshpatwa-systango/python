
# Python Operators

# Arithmetic Operators
a, b = 10, 3
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b)
print(a % b)
print(a ** b)

# Comparison Operators
print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# Logical Operators
x, y = True, False
print(x and y)
print(x or y)
print(not x)

# Assignment Operators
c = 5
c += 2
c -= 1
c *= 3
c /= 2

# Bitwise Operators
d, e = 5, 3
print(d & e)
print(d | e)
print(d ^ e)
print(~d)
print(d << 1)
print(d >> 1)

